package JavaProperties;

public class Box {
    int height;
    int width;
    int length;

    public Box() {
        this.height = -1;
        this.width = -1;
        this.length = -1;
    }

    public Box(int height, int width, int length) {
        this.height = height;
        this.width = width;
        this.length = length;
    }
}
